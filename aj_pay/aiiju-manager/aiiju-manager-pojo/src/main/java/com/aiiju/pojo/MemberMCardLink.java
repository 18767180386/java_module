package com.aiiju.pojo;

import java.io.Serializable;

/**
 * 
 * @ClassName: MCardMemberLink 
 * @Description: 会员卡与会员的关联中间表对象
 * @author 小飞 
 * @date 2016年11月12日 上午9:56:47 
 *
 */
public class MemberMCardLink implements Serializable{

	private static final long serialVersionUID = 2577044546021953208L;

	private Long mCardId;
	
	private Long memberId;
	/**
	 * 每个会员对于不同会员卡的唯一标识
	 */
	private String code;

	public Long getmCardId() {
		return mCardId;
	}

	public void setmCardId(Long mCardId) {
		this.mCardId = mCardId;
	}

	public Long getMemberId() {
		return memberId;
	}

	public void setMemberId(Long memberId) {
		this.memberId = memberId;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}
	
}
