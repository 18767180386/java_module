package com.aiiju.pojo;

import java.io.Serializable;
import java.util.Date;
import java.util.List;

/**
 * 
 * @ClassName: Member
 * @Description: 会员
 * @author 小飞
 * @date 2016年11月10日 上午9:39:13
 *
 */
public class Member implements Serializable {

    private static final long serialVersionUID = 5784358171055525997L;

    private Long id;

    /**
     * 手机号
     */
    public String phone;

    /**
     * 姓名
     */
    public String name;

    /**
     * 性别 1：男 2：女
     */
    public Byte gender;

    /**
     * 生日
     */
    public Date birthday;

    /**
     * 省份
     */
    public String province;

    /**
     * 城市
     */
    public String city;

    /**
     * 区域
     */
    public String area;

    /**
     * 地址
     */
    private String address;

    /**
     * 备注
     */
    public String remark;

    /**
     * 创建时间
     */
    public Date createDate;

    /**
     * 修改时间
     */
    public Date modifyDate;

    /**
     * 店铺编号
     */
    private String storeId;

    private List<MCard> cardList;

    private List<Coupon> couponList;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Byte getGender() {
        return gender;
    }

    public void setGender(Byte gender) {
        this.gender = gender;
    }

    public Date getBirthday() {
        return birthday;
    }

    public void setBirthday(Date birthday) {
        this.birthday = birthday;
    }

    public String getProvince() {
        return province;
    }

    public void setProvince(String province) {
        this.province = province;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public Date getCreateDate() {
        return createDate;
    }

    public void setCreateDate(Date createDate) {
        this.createDate = createDate;
    }

    public Date getModifyDate() {
        return modifyDate;
    }

    public void setModifyDate(Date modifyDate) {
        this.modifyDate = modifyDate;
    }

    public String getStoreId() {
        return storeId;
    }

    public void setStoreId(String storeId) {
        this.storeId = storeId;
    }

    public List<MCard> getCardList() {
        return cardList;
    }

    public void setCardList(List<MCard> cardList) {
        this.cardList = cardList;
    }

    public List<Coupon> getCouponList() {
        return couponList;
    }

    public void setCouponList(List<Coupon> couponList) {
        this.couponList = couponList;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

}
